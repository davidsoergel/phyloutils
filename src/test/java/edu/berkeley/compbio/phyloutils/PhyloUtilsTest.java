public class PhyloUtilsTest
	{
	@Test
	public void ciccarelliExactDistancesAreComputedCorrectly()
		{
		double d = PhyloUtils.exactDistanceBetween(217992,562);
		assert d == 0.000221;

		d = PhyloUtils.exactDistanceBetween(217992, 59919);
		assert d == 1.47739;
		}

	@Test
	public void nearestKnownAncestorWorks() throws PhyloUtilsException
		{
		assert PhyloUtils.nearestKnownAncestor(243277) == 666;
		}

	@Test
	public void findTaxonByNameWorks()
	{
	assert NcbiTaxonomyName.findByName("Myxococcus xanthus").getTaxon().getTaxId() == 34;
	}
	}
